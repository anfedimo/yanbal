package co.sistemcobro.yanbal.bean;

public class InformacionComplementaria {

	private Integer idcargue;
	private String nombreCargue;
	private String descripcion;
	private String valor;
	
	public Integer getIdcargue() {
		return idcargue;
	}
	public void setIdcargue(Integer idcargue) {
		this.idcargue = idcargue;
	}
	public String getNombreCargue() {
		return nombreCargue;
	}
	public void setNombreCargue(String nombreCargue) {
		this.nombreCargue = nombreCargue;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getValor() {
		return valor;
	}
	public void setValor(String valor) {
		this.valor = valor;
	}
	
	
	
}
